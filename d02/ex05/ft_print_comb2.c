int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	char a[2];

	a[0] = 0;
	a[1] = 0;
	while (a[0] < 100)
	{
		while (a[1] < 100)
		{
			if (a[0] != a[1])
			{
				ft_putchar((a[0] / 10) + '0');
				ft_putchar((a[0] % 10) + '0');
				ft_putchar(' ');
				ft_putchar((a[1] / 10) + '0');
				ft_putchar((a[1] % 10) + '0');
				if (a[0] != 98)
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
			}
			a[1]++;
		}
		a[1] = a[0]++ + 1;
	}
}
